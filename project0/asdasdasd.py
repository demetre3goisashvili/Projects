num1 = int(input("please enter your num1: "))
choice = input("please make your choice: ")
num2 = int(input("please enter your num2: "))

if choice == "+":
    print(num1 + num2)
elif choice == "-":
    print(num1 - num2)
elif choice == "*":
    print(num1 * num2)
elif choice == "/":
    print(num1 / num2)
 






    


    

    
